async def recent_entries(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """
    –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /recent.
    –û—Ç–æ–±—Ä–∞–∂–∞–µ—Ç –Ω–µ–¥–∞–≤–Ω–∏–µ –∑–∞–ø–∏—Å–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    """
    # –ó–∞–≤–µ—Ä—à–∞–µ–º –≤—Å–µ –∞–∫—Ç–∏–≤–Ω—ã–µ –¥–∏–∞–ª–æ–≥–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–µ—Ä–µ–¥ –Ω–∞—á–∞–ª–æ–º –Ω–æ–≤–æ–≥–æ
    chat_id = update.effective_chat.id
    end_all_conversations(chat_id)

    logger.info(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {chat_id} –∑–∞–ø—Ä–æ—Å–∏–ª –Ω–µ–¥–∞–≤–Ω–∏–µ –∑–∞–ø–∏—Å–∏")

    # –ü–æ–ª—É—á–µ–Ω–∏–µ –∑–∞–ø–∏—Å–µ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    entries = get_user_entries(chat_id)

    if not entries:
        await update.message.reply_text(
            "–£ –≤–∞—Å –µ—â–µ –Ω–µ—Ç –∑–∞–ø–∏—Å–µ–π –≤ –¥–Ω–µ–≤–Ω–∏–∫–µ –∏–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞—Ç—å –¥–∞–Ω–Ω—ã–µ.",
            reply_markup=MAIN_KEYBOARD
        )
        return

    # –û–±–æ–≥–∞—â–∞–µ–º –∑–∞–ø–∏—Å–∏ –ø—Ä–∏–≤—è–∑–∞–Ω–Ω—ã–º–∏ –≤–ø–µ—á–∞—Ç–ª–µ–Ω–∏—è–º–∏
    try:
        from src.data.storage import _get_db_connection
        from src.utils.formatters import enrich_entries_with_impressions

        conn = _get_db_connection()
        entries = enrich_entries_with_impressions(entries, chat_id, conn)
    except Exception as e:
        logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–æ–≥–∞—Ç–∏—Ç—å –∑–∞–ø–∏—Å–∏ –≤–ø–µ—á–∞—Ç–ª–µ–Ω–∏—è–º–∏: {e}")

    # –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–∫–∞ —Å–ø–∏—Å–∫–∞ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –∑–∞–ø–∏—Å–µ–π
    formatted_entries = format_entry_list(entries)

    await update.message.reply_text(
        formatted_entries,
        reply_markup=MAIN_KEYBOARD
    )


